
public class Game {
	String name;
	int maxPlayers;
	public Game(String name, int maxPlayers) {
		this.name = name;
		this.maxPlayers = maxPlayers;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getMaxPlayers() {
		return maxPlayers;
	}
	public void setMaxPlayers(int maxPlayers) {
		this.maxPlayers = maxPlayers;
	}
	

}
